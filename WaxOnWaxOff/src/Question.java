
class Question {
	
	private String questionText;
	private String mcA;
	private String mcB;
	private String mcC;
	private String mcD;
	private String answer;
	
	public Question(String questionText, String mcA, String mcB, String mcC, String mcD, String answer) {
		
		this.questionText = questionText;
		this.mcA = mcA;
		this.mcB = mcB;
		this.mcC = mcC;
		this.mcD = mcD;
		this.answer = answer;
		
	}

}
